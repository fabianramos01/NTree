package models;

import java.util.ArrayList;

public class NodeFather extends Node {

	public NodeFather(String info) {
		super(info);
	}
	
	public void addChild(Node nodeNTree) {
		addChild(nodeNTree);
	}
	
	public ArrayList<Node> getChilds() {
		return getChilds();
	}
	
	@Override
	public String getInfo() {
		return super.getInfo();
	}
}